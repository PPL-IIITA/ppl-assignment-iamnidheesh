class Gift :

	'defines a gift'
	def __init__(self,name,price,value,types):
		'initialises attributes of Gift class'
		self.name = name
		"""@ivar: name of gift"""
		self.price = price
		"""@ivar: price of gift"""
		self.value = value
		"""@ivar: value of gift"""
		self.types = types
		"""@ivar: type of gift."""

